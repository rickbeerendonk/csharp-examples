char charValue = 'a';
Console.WriteLine(charValue);                              // a
Console.WriteLine(charValue.GetType().FullName);           // System.Char

// Special
Console.WriteLine('\t');                                   // (tab)
Console.WriteLine('\b');                                   // (backspace)
Console.WriteLine('\n');                                   // (new-line)
Console.WriteLine('\r');                                   // (return)
Console.WriteLine('\'');                                   // '
Console.WriteLine('\"');                                   // "
Console.WriteLine('\\');                                   // \
Console.WriteLine('$');                                    // $
Console.WriteLine('\uFF00');
