int x = 10;
Console.WriteLine($"x = {x}");

x += 5; // x = x + 5
Console.WriteLine($"x += 5: {x}");

x -= 3; // x = x - 3
Console.WriteLine($"x -= 3: {x}");

x *= 2; // x = x * 2
Console.WriteLine($"x *= 2: {x}");

x /= 4; // x = x / 4
Console.WriteLine($"x /= 4: {x}");

x %= 3; // x = x % 3
Console.WriteLine($"x %= 3: {x}");
